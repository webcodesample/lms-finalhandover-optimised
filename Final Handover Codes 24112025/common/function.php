//function to copy crn documents to lrn
function copyCRNDocsToLRN($crn,$lrn,$con)
{
	$crnDocumentPaths = glob("../crn_documents/".$crn."/*.*");

	if(count($crnDocumentPaths) > 0)
	{
		$_REQUEST['lrn'] = $lrn;
		$lrn_folder = "../lrn/la".$_REQUEST['lrn']."/";

		//check whether urn folder exist or not; if not then create lrn folder with full permission
		if (!file_exists($lrn_folder)) 
		mkdir($lrn_folder, 0777, true);

		foreach ($crnDocumentPaths as $crnDocumentPath)
		{
			$documentName = pathinfo($crnDocumentPath, PATHINFO_FILENAME);
			$documentExt = pathinfo($crnDocumentPath, PATHINFO_EXTENSION);
			$documentNewName = $documentName."_".strtotime('now').".".$documentExt;
			copy($crnDocumentPath, $lrn_folder.$documentNewName);
			recordLRNDocUploadHistory($documentNewName,$con);
		}
	}
}

//function to check column is updated or not
function validateColumnUpdation($column,$columnValue,$updated_column_array)
{
	if(in_array($column,$updated_column_array))
	return "<span class='text-danger'>".$columnValue."</span>";
	else
	return $columnValue;
}

function createInput($input,$inputVal,$label_design,$input_design,$field_visibility='',$type='text')
{
    if($input == 'dob')
    $label = 'Date of Birth';
	else if($input == 'company_doi' || $input == 'doi')
    $label = 'Incorporated On';
	else if($input == 'company_turnover')
    $label = 'Annual Turnover';
	else if($input == 'required_loan_amount')
    $label = 'Amount';
    else if(str_contains($input, 'commission_percent'))
    $label = 'Comission %';
    else if(str_contains($input, '_id'))
    $label = ucwords(str_replace('_id','',$input));
    else if(str_contains($input, 'company_'))
    $label = ucwords(str_replace('company_','',$input));
    else if(str_contains($input, 'guarantor_'))
    $label = ucwords(str_replace('guarantor_','',$input));
    else
	$label = $input;
    
	$label = ucwords(str_replace('_',' ',$label));

	if($type == 'textarea')
    $inputField = "<textarea ".$input_design." name='".$input."' id='".$input."' placeholder='".$label."' ".$field_visibility.">".$inputVal."</textarea>";
	else
	$inputField = "<input type='".$type."' ".$input_design." name='".$input."' id='".$input."' value='".$inputVal."' placeholder='".$label."' ".$field_visibility.">";

	echo "<div class='d-flex'>
        <div ".$label_design.">".$label."</div>
		".$inputField."
		</div>";
}


