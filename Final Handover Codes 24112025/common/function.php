//function to generate inputs name array for comparision
function generateInputNamesArray()
{
	$inputNames = [];

	foreach($_REQUEST AS $key => $value)
	{
		if(str_contains($key,'_old'))
		array_push($inputNames,$key);
	}

	return $inputNames;
}

//function to generate updated column names array
function generateUpdatedColumns($inputNames)
{
	$updated_columns = [];
	foreach($inputNames AS $column)
	{
		$input = str_replace('_old','',$column);
		if(isset($_REQUEST[$input]) && $_REQUEST[$column] != $_REQUEST[$input])
		array_push($updated_columns,$input);
	}
	return $updated_columns;
}

//insert row in leads_activity_history
function recordLRNActivityHistory($con)
{
	//get latest updated details about requested lrn
	$lrn_latest_data = getLatestUpdateRow('lrn',$_REQUEST['lrn'],'lrn_audit_history',$con);
	unset($lrn_latest_data['id']);
	
	//insert leads_activity_history
	$activty_info_array = [
