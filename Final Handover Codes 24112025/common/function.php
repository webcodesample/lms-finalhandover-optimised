function extractIdFromInput($input)
{
	$input_array = explode(' - ',$input);

	if(count($input_array) > 1)
	return $input_array[1];
	else
	return $input;
}

function extractIdFromInputs($inputsArray)
{
	$result = [];
	foreach($inputsArray AS $input)
	{
		$key = str_replace('_old','',$input);
		$result[$key] = extractIdFromInput($_REQUEST[$key]);
	}
	return $result;
}

function generateTypeDropdown($selectedType = '')
{
	$type_options = "<option value=''>Type</option>";
	$types = ['Individual','Corporate'];
	for($i=0; $i<count($types); $i++)
	{
		if($i+1 == $selectedType)
		$type_options .= "<option value=".($i+1)." selected>".$types[$i]."</option>";
		else
		$type_options .= "<option value=".($i+1).">".$types[$i]."</option>";
	}
	return $type_options;
}

