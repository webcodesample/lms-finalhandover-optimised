					'datentime' => strtotime('now'),
					];
	insertData('lrn_activity_history',$activty_info_array,$con);
}

//insert row in lrn_audit_history
function recordLRNAuditHistory($updated_columns,$con)
{
	//get latest updated details about requested lrn
	$lrn_latest_data = getLatestUpdateRow('lrn',$_REQUEST['lrn'],'lrn_audit_history',$con);
	unset($lrn_latest_data['id']);

	//insert urn_audit_history
	foreach($lrn_latest_data AS $key => $value)
	{
		if(in_array($key,$updated_columns) || ($key == 'updation_remark'))
		$lrn_latest_data[$key] = $_REQUEST[$key];
		else if($key == 'datentime')
		$lrn_latest_data[$key] = strtotime('now');
		else if($key == 'updated_columns')
		$lrn_latest_data[$key] = implode(',',$updated_columns);
	}
	$lrn_updated_data = $lrn_latest_data;

	insertData('lrn_audit_history', $lrn_updated_data, $con);
}

